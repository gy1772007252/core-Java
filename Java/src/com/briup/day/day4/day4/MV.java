package com.briup.day.day4.day4;

/**
 * 移位运算符
 */
public class MV {

    public static void main(String[] args) {

        System.out.println(8 >> 2);// 8 / 2^2
        System.out.println(8 << 2);// 8 * 2^2

        System.out.println(-8 >> 2);
        System.out.println(-8 << 2);

        System.out.println(9 >>> 1);
        System.out.println(-9 >>> 1);

    }

}
